<h1>Altas por CSV</h1>
<p>Hola, se encuentra en la herramienta con la que podrá realizar inserción de habilidades mediante la carga de un fichero en formatto CSV</p>
<p>Caracteristicas de un fichero CSV:</p>
<ul>
    <li>Cada fila es un registro diferente.</li>
    <li>Los campos deben de ir separados mediante ; (punto y coma).</li>
    <li>Los campos son los siguientes:</li>
    <ol>
        <li>Nombre de la habilidad</li>
        <li>Tipo de la habilidad</li>
        <li>Descripción</li>
    </ol>
    <li>Los campos citados deben ir en el mismo orden incicado en la lista del punto anterior.</li>
</ul>

